  public Dictionary<string, List<string>> GetProfessionalCategories()
  {
    return this.professionalCategories;
  }

  public string GetProfessionalGroupByJobName(string jobName)
  {
    if (string.IsNullOrWhiteSpace(jobName))
      return null;

    jobName = jobName.ToLower().Trim();

    string bestMatch = null;
    int bestLength = 0;

    foreach (var group in professionalCategories)
    {
      foreach (var keyword in group.Value)
      {
        var k = keyword.ToLower();

        // точное совпадение — сразу возврат
        if (jobName == k)
          return group.Key;

        // частичное — ищем лучшее
        if (jobName.Contains(k) && k.Length > bestLength)
        {
          bestLength = k.Length;
          bestMatch = group.Key;
        }
      }
    }

    return bestMatch;
  }
